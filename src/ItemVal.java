

public class ItemVal {
    private String itemID;
    private String title;
    private String genre;
    private String releaseDate;
    private String artist;
    private String price;

    public ItemVal(String itemID, String title, String genre, String releaseDate, String artist, String price) {
        this.itemID = itemID;
        this.title = title;
        this.genre = genre;
        this.releaseDate = releaseDate;
        this.artist = artist;
        this.price = price;
    }
}
